#include "libft.h"

int	ft_found_char(const char *s, char c)
{
	int	i;

	if (!s || !c)
		return (0);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (1);
		i++;
	}
	return (0);
}
