#include "libft.h"

void	ft_free_tab_2d(char **tab)
{
	int	i;
	int	j;

	if (!tab)
		return ;
	i = 0;
	j = 0;
	while (tab[i])
	{
		free(tab[i]);
	i++;
	}
	free(tab);
}
